package de.hhn.it.devtools.apis.spaceinvaders;

/**
 * Realizes the Direction on the GameField.
 */
public enum Direction {
    /**
     * Moves Entities UP.
     */
    UP,
    /**
     * Moves Entities DOWN.
     */
    DOWN,
    /**
     * Moves Entities LEFT.
     */
    LEFT,
    /**
     * Moves Entities RIGHT.
     */
    RIGHT
}
